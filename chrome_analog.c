static const struct drm_encoder_funcs chrome_analog_enc_funcs = {
	.destroy = drm_encoder_cleanup,
};

/* Power down the DAC */
static void
chrome_analog_dpms(struct drm_encoder *encoder, int mode)
{
	struct drm_crtc *crtc = encoder->crtc;
	struct drm_chrome_private *dev_priv;
	struct chrome_crtc *iga;
	u8 dacmode;

	dev_priv = encoder->dev->dev_private;
	iga = &dev_priv->iga[0];
	if (crtc != &iga->crtc)
		iga = &dev_priv->iga[1];
	dacmode = crtc_inb(iga->vga_regs, 0x36);

	switch (mode) {
	case DRM_MODE_DPMS_SUSPEND:
		dacmode |= 0x20;	// VSync off  
		break;
	case DRM_MODE_DPMS_STANDBY:
		dacmode |= 0x10;	// HSync off	
		break;
	case DRM_MODE_DPMS_OFF:
		dacmode |= 0x30;	// HSync and VSync off
		break;
	case DRM_MODE_DPMS_ON:
	default:
		dacmode &= ~0x30;
		break;
	}

	crtc_outb(iga->vga_regs, 0x36, (dacmode & 0x30));
}

/* Pass our mode to the connectors and the CRTC to give them a chance to
 * adjust it according to limitations or connector properties, and also
 * a chance to reject the mode entirely. Usefule for things like scaling.
 */
static bool
chrome_analog_mode_fixup(struct drm_encoder *encoder,
		 struct drm_display_mode *mode,
		 struct drm_display_mode *adjusted_mode)
{
	/* Could sometimes be 8 */
	adjusted_mode->hdisplay = (adjusted_mode->hdisplay + 15) & ~15;

	drm_mode_set_crtcinfo(adjusted_mode, 0);
	return true;
}

