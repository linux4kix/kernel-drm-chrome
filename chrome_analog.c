{
	struct drm_device *dev = connector->dev;

	//if (drm_do_probe_ddc_edid(connector))
		return connector_status_connected;

	return connector_status_unknown;
}

static void
chrome_analog_destroy(struct drm_connector *connector)
{
	//chrome_i2c_destroy(connector->dev, DRM_MODE_CONNECTOR_VGA);
	drm_sysfs_connector_remove(connector);
	drm_connector_cleanup(connector);
	kfree(connector);
}

static int
chrome_analog_set_property(struct drm_connector *connector,
		  struct drm_property *property,
		  uint64_t value)
{
	struct drm_device *dev = connector->dev;

	if (property == dev->mode_config.dpms_property && connector->encoder)
		chrome_analog_dpms(connector->encoder, (uint32_t)(value & 0xf));
	return 0;
}

static const struct drm_connector_funcs chrome_analog_connector_funcs = {
	.detect = chrome_analog_detect,
	.fill_modes = drm_helper_probe_single_connector_modes,
	.destroy = chrome_analog_destroy,
	.set_property = chrome_analog_set_property,
};

/* This function test if the drm_display_modes generated by a display
 * monitor that is connected is supported by the graphics card.
 */
static int
chrome_analog_mode_valid(struct drm_connector *connector,
			struct drm_display_mode *mode)
{
	//struct drm_device *dev = connector->dev;

	if ((mode->flags & DRM_MODE_FLAG_INTERLACE) &&
	    !connector->interlace_allowed)
		return MODE_NO_INTERLACE;

	return MODE_OK;
}

